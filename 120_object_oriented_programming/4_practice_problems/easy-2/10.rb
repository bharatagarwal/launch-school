# What are the benefits of using Object Oriented Programming in Ruby? Think of as many as you can.

# my answer:

# Allows you to reuse code (inheritance)
# Allows you to hide certain portions of code away from the main user.
# Allows for easier change later down the road by creating independent islands of logic that can interact with each other by sending messages.
# Allows you to represent bits of code the way you want. (polymorphism)

